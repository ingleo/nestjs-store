import { Entity, PrimaryGeneratedColumn, Column } from 'typeorm';

import { DateAt } from '../../common/entities/date-at.entity';

@Entity()
export class Product {
  @PrimaryGeneratedColumn()
  id: number;

  @Column({ type: 'varchar', length: 255, unique: true })
  name: string;

  @Column({ type: 'text' })
  description: string;

  @Column({ type: 'int' })
  price: number;

  @Column({ type: 'int' })
  stock: number;

  @Column({ type: 'varchar' })
  image: string;

  @Column(() => DateAt)
  date: DateAt;
}
